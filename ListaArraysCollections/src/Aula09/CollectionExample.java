package Aula09;
import java.util.ArrayList;
import java.util.List;

public class CollectionExample {
	 public static void main(String[] args) {
	        List<String> cities = new ArrayList<>(); // Cria uma list vazia de Strings

	        cities.add("Campinas"); // Adiciona "Campinas" à lista
	        cities.add("São Paulo"); // Adiciona "São Paulo" à lista
	        cities.add("Jaguariúna"); // Adiciona "Jaguariúna à lista

	        System.out.println(cities); // Exibe os elementos da lista
	    }
}
